    public void malzemeGoruntule(){
    //malzeme görüntüleme metodu
        model1.setRowCount(0);
        ArrayList<Malzeme> malzemeler = new ArrayList<Malzeme>();
        malzemeler = stokislem.malzemeGetir();
        if(malzemeler != null) {
        
            for (Malzeme malzeme : malzemeler){
                //private tanımlı değişkene erişim 
                Object[] eklenecek = {malzeme.getId(), malzeme.getIsim(), malzeme.getBarkod(), malzeme.getTur(), malzeme.getMiktar(), malzeme.getFiyat(), malzeme.getKayittarihi()};
                model1.addRow(eklenecek);
            }
        }
        
    } 

