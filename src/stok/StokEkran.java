    private void Btn_malzemeEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_malzemeEkleActionPerformed
        // TODO add your handling code here:
          Lbl_stokmesaj.setText("");
        if (Txt_isim.getText().equals("") || Txt_barkod.getText().equals("") || Txt_tur.getText().equals("")
                || Txt_miktar.getText().equals("") || Txt_fiyat.getText().equals("") || Txt_kayitTarihi.getText().equals("")){
        //Eklenecek malzemeye ait tüm bilgilerin eksiksiz olması kontrol ediliyor
            Lbl_stokmesaj.setText("Malzeme Eklenemedi Tüm Alanları Doldurunuz...");
            
        }
        else {
        //Bilgiler eksiksiz ise verileri çekilip
        //Malzeme işlemleri sınıdında malzemeEkle metoduna 
        //gönderilip veritabanına ekleniyor
        String isim = Txt_isim.getText();
        String barkod = Txt_barkod.getText();
        String tur = Txt_tur.getText();
        String miktar = Txt_miktar.getText();
        String fiyat = Txt_fiyat.getText();
        String kayittarihi = Txt_kayitTarihi.getText();
        
        stokislem.malzemeEkle(isim, barkod, tur, miktar, fiyat, kayittarihi);
        //malzeme ekleme sonrası veri tabanı tekrar görüntüleniyor
        malzemeGoruntule();
        Lbl_stokmesaj.setText("MalzemeEklendi...");
         
            
        }
        
    }//GEN-LAST:event_Btn_malzemeEkleActionPerformed

