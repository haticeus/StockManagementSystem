    private void Btn_malzemeSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_malzemeSilActionPerformed
        // TODO add your handling code here:
         Lbl_stokmesaj.setText("");
        int selectedrow = Malzeme_tablosu.getSelectedRow();
        //tablodan silinecek eleman seçiliyor
        if (selectedrow == -1) {
            if (model1.getRowCount() == 0) {
                Lbl_stokmesaj.setText("Tablo Boş Silinecek Malzeme Yok");
                //tabloda seçilecek eleman yoksa uyarı veriyor
            } else {
                Lbl_stokmesaj.setText("Lütfen Silinecek Malzemeyi Seçiniz! ");  
                //tabloda seçili eleman yoksa uyarı veriyor
            }
        }
        else {
            int id = (int)model1.getValueAt(selectedrow, 0);
            sil.malzemeSil(id);
            //Inner Class tan olşan sil objesinden malzemeSil metodu çağrılıyor
            malzemeGoruntule();
            Lbl_stokmesaj.setText("Malzeme Silme İşlemi Gerçekleşti");
        }
    }//GEN-LAST:event_Btn_malzemeSilActionPerformed

